package com.proyecto.modelo;

public class Informatica {
	private int idInformatica;
	private String tipoInformatica;
	private String nivelInformatica;

	// Get and Set
	public int getIdInformatica() {
		return idInformatica;
	}

	public void setIdInformatica(int idInformatica) {
		this.idInformatica = idInformatica;
	}

	public String getTipoInformatica() {
		return tipoInformatica;
	}

	public void setTipoInformatica(String tipoInformatica) {
		this.tipoInformatica = tipoInformatica;
	}

	public String getNivelInformatica() {
		return nivelInformatica;
	}

	public void setNivelInformatica(String nivelInformatica) {
		this.nivelInformatica = nivelInformatica;
	}

}
